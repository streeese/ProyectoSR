    private void jbtn_actualizarpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_actualizarpActionPerformed
        int precio, id;
        String nombre,marca;
        
        id = Integer.parseInt(this.jtxt_idBuscarP.getText());
        precio = Integer.parseInt(this.jtxt_preciop.getText());
        nombre = this.jtxt_nombrep.getText();
        marca = this.jtxt_marca.getText();
        
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyy");
        Date fecha = null;
        
        try {
            fecha= formato.parse(this.jtxt_fechap.getText());
        } catch (ParseException e) {
            
            System.out.println("Error al actualizar fecha" + e.getMessage());
        }
        AlimentoPerro alimentoPerro = new AlimentoPerro(id, nombre, marca, fecha, precio, true);
        
        registro.actualizar(alimentoPerro);
        JOptionPane.showMessageDialog(null, "Datos Actualizados Correctamente", "Aviso", 2);
    }//GEN-LAST:event_jbtn_actualizarpActionPerformed

