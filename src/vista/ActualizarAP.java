    private void jbtn_buscarPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_buscarPActionPerformed
       int id = Integer.parseInt(this.jtxt_idBuscarP.getText());
       
       AlimentoPerro alimentoPerro = registro.buscarAlimentoPerro(id);
       
       String nombre = alimentoPerro.getNombre();
       String marca = alimentoPerro.getMarca();
       int precio = alimentoPerro.getPrecio();
       Date fecha = alimentoPerro.getFechaIngreso();
       
       this.jtxt_nombrep.setText(nombre);
       this.jtxt_marca.setText(marca);
       this.jtxt_preciop.setText(String.valueOf(precio));
       this.jtxt_fechap.setText(""+fecha);
    }//GEN-LAST:event_jbtn_buscarPActionPerformed

